#include "../includes/libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int				counter;
	unsigned char	*s_c;

	if ((!s) || (!fd))
		return ;
	counter = -1;
	s_c = ((unsigned char *)s);
	while (s_c[++counter] != '\0')
		write(fd, &s_c[counter], 1);
	write(fd, "\n", 1);
}
